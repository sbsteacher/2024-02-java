package com.green.day03.ch05;

public class ForInFor2 {
    public static void main(String[] args) {
        System.out.println("------------");

        for(int z=0; z<5; z++) {
            System.out.printf("[%d, %d]", 0, z);
        }
        System.out.println();

        System.out.println("------------");

        for(int z=0; z<5; z++) {
            System.out.printf("[%d, %d]", 1, z);
        }
        System.out.println();

        System.out.println("------------");

        for(int z=0; z<5; z++) {
            System.out.printf("[%d, %d]", 2, z);
        }
        System.out.println();

        System.out.println("------------");

        for(int z=0; z<5; z++) {
            System.out.printf("[%d, %d]", 3, z);
        }
        System.out.println();

        System.out.println("------------");

        for(int z=0; z<5; z++) {
            System.out.printf("[%d, %d]", 4, z);
        }
        System.out.println();
    }
}
